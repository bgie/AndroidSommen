package les.syntra.androidsommen.logic;

/* Ontwerp velden:
	> Question: string
	> Answer: double
	> Exercise(question, answer)
 */
public class Exercise {
	String Question = "";
	double Answer = 0;
	
	public Exercise(String aQuestion, double aAnswer)
	{
		Question = aQuestion;
		Answer = aAnswer;
	}
}
